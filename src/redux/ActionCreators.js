//In the "Fetch From Server" lesson, we learned how to get data from a server using fetch 
//simply by providing fetch with a URL.  In the "Fetch Post Comment" lesson, we are 
//learning how to post data to a server using fetch.  Comments from the user entered on the 
//CampsiteInfo page will be posted to the C:\Users\ChrisPhillips\Desktop\NucampFolder\json-server\db.json file,
//and when the page is refreshed any comments added will persist and remain displayed on the screen and not go away like
//before. 

export const fetchCampsites = () => dispatch => {

    dispatch(campsitesLoading());
    
    return fetch(baseUrl + 'campsites')
        .then(response => {
            if (response.ok) {
                return response;
            } else {
                const error = new Error(`Error ${response.status}: ${response.statusText}`);
                error.response = response;
                throw error;
            }
        },
        error => {
            const errMess = new Error(error.message);
            throw errMess;
        }
    )
    // calling fetch() and returning the result.  baseUrl is an imported file
    // and campsite is the location for the resource we want.  A call to fetch returns 
    // a promise.
        .then(response => response.json())  
    //When that promise is resolved, the then() method will use the
    // response.json() method to convert the response from json to javascript, and that 
    // javascript will be an array of campsites.  The json method returns a new 
    // promise for which the converted javascript array is the new response value when 
    // it resolves. 
        .then(campsites => dispatch(addCampsites(campsites)))
     // grabbing that javascript array is the first campsites argument once that promise
     // resolves.  Then we can dispatch that campsites argument with the addCampsites
     // action creator to be used as its payload.  we won't be dealeing with errors
     // or adding a catch() mehtod to this fetch() yet but will be coming.
        .catch(error => dispatch(campsitesFailed(error.message)));
};

export const campsitesLoading = () => ({
    type: ActionTypes.CAMPSITES_LOADING
});

export const campsitesFailed = errMess => ({
    type: ActionTypes.CAMPSITES_FAILED,
    payload: errMess
});

export const addCampsites = campsites => ({
    type: ActionTypes.ADD_CAMPSITES,
    payload: campsites
});

export const fetchComments = () => dispatch => {
    return fetch(baseUrl + 'comments')
        .then(response => {
                if (response.ok) {
                    return response;
                } else {
                    const error = new Error(`Error ${response.status}: ${response.statusText}`);
                    error.response = response;
                    throw error;
                }
            },
            error => {
                const errMess = new Error(error.message);
                throw errMess;
            }
        )
        .then(response => response.json())
        .then(comments => dispatch(addComments(comments)))
        .catch(error => dispatch(commentsFailed(error.message)));
};

export const commentsFailed = errMess => ({
    type: ActionTypes.COMMENTS_FAILED,
    payload: errMess
});

export const addComments = comments => ({
    type: ActionTypes.ADD_COMMENTS,
    payload: comments
});

export const addComment = comment => ({
    type: ActionTypes.ADD_COMMENT,
    payload: comment
});

export const postComment = (campsiteId, rating, author, text) => dispatch => {
    
    const newComment = {
        campsiteId: campsiteId,
        rating: rating,
        author: author,
        text: text
    };
    newComment.date = new Date().toISOString();

    return fetch(baseUrl + 'comments', {
            method: "POST",
            body: JSON.stringify(newComment),
            headers: {
                "Content-Type": "application/json"
            }
        })
        .then(response => {
                if (response.ok) {
                    return response;
                } else {
                    const error = new Error(`Error ${response.status}: ${response.statusText}`);
                    error.response = response;
                    throw error;
                }
            },
            error => { throw error; }
        )
        .then(response => response.json())
        .then(response => dispatch(addComment(response)))
        .catch(error => {
            console.log('post comment', error.message);
            alert('Your comment could not be posted\nError: ' + error.message);
        });
};

